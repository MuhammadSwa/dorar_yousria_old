بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿قُلۡ يَـٰٓأَيُّهَا ٱلۡكَٰفِرُونَ۝١لَآ أَعۡبُدُ مَا تَعۡبُدُونَ۝٢وَلَآ أَنتُمۡ عَٰبِدُونَ مَآ أَعۡبُدُ۝٣وَلَآ أَنَا۠ عَابِدٌ مَّا عَبَدتُّمۡ۝٤وَلَآ أَنتُمۡ عَٰبِدُونَ مَآ أَعۡبُدُ۝٥لَكُمۡ دِينُكُمۡ وَلِيَ دِينِ۝٦﴾ [الكافرون: 1-6].

بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿إِذَا جَآءَ نَصۡرُ ٱللَّهِ وَٱلۡفَتۡحُ۝١وَرَأَيۡتَ ٱلنَّاسَ يَدۡخُلُونَ فِي دِينِ ٱللَّهِ أَفۡوَاجٗا۝٢فَسَبِّحۡ بِحَمۡدِ رَبِّكَ وَٱسۡتَغۡفِرۡهُۚ إِنَّهُۥ كَانَ تَوَّابَۢا۝٣﴾ [النصر: 1-3].

بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿قُلۡ هُوَ ٱللَّهُ أَحَدٌ۝١ٱللَّهُ ٱلصَّمَدُ۝٢لَمۡ يَلِدۡ وَلَمۡ يُولَدۡ۝٣وَلَمۡ يَكُن لَّهُۥ كُفُوًا أَحَدُۢ۝٤﴾ [الإخلاص: 1-4] (ثلاثًا).

بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿قُلۡ أَعُوذُ بِرَبِّ ٱلۡفَلَقِ۝١مِن شَرِّ مَا خَلَقَ۝٢وَمِن شَرِّ غَاسِقٍ إِذَا وَقَبَ۝٣وَمِن شَرِّ ٱلنَّفَّـٰثَٰتِ فِي ٱلۡعُقَدِ۝٤وَمِن شَرِّ حَاسِدٍ إِذَا حَسَدَ۝٥﴾ [الفلق:1-5] (ثلاثًا).

بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿قُلۡ أَعُوذُ بِرَبِّ ٱلنَّاسِ۝١مَلِكِ ٱلنَّاسِ۝٢إِلَٰهِ ٱلنَّاسِ۝٣مِن شَرِّ ٱلۡوَسۡوَاسِ ٱلۡخَنَّاسِ۝٤ٱلَّذِي يُوَسۡوِسُ فِي صُدُورِ ٱلنَّاسِ۝٥, مِنَ ٱلۡجِنَّةِ وَٱلنَّاسِ۝٦﴾ [الناس: 1-6] (ثلاثًا).

اللَّهُمَّ إِنِّي أَعُوذُ بِكَ أَنْ أُشْرِكَ بِكَ وَأَنَا أَعْلَمُ، وَأَسْتَغْفِرُكَ لِمَا لَا أَعْلَمُ (ثلاثًا). [تقرأ مرة واحدة للمتعجل]

اللَّهُمَّ إِنِّي أَعُوذُ بِكَ مِنَ الْهَمِّ وَالْحَزَنِ، وَأَعُوذُ بِكَ مِنَ الْعَجْزِ وَالْكَسَلِ، وَأَعُوذُ بِكَ مِنَ الْجُبْنِ وَالْبُخْلِ، وَأَعُوذُ بِكَ مِنْ غَلَبَةِ الدَّيْنِ وَقَهْرِ الرِّجَالِ (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

اللَّهُمَّ إِنِّي أَعُوذُ بِكَ مِنَ الْكُفْرِ وَالْفَقْرِ وَأَعُوذُ بِكَ مِنْ عَذَابِ الْقَبْرِ، لَا إِلَهَ إِلَّا أَنْتَ (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

اللَّهُمَّ عَافِنِي فِي بَدَنِي، اللَّهُمَّ عَافِنِي فِي سَمْعِي، اللَّهُمَّ عَافِنِي فِي بَصَرِي، لَا إِلَهَ إِلَّا أَنْتَ (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

اللَّهُمَّ أَنْتَ رَبِّي، لاَ إِلَهَ إِلَّا أَنْتَ، خَلَقْتَنِي وَأَنَا عَبْدُكَ (أَمَتُكَ)، وَأَنَا عَلَى عَهْدِكَ وَوَعْدِكَ مَا اسْتَطَعْتُ، أَعُوذُ بِكَ مِنْ شَرِّ مَا صَنَعْتُ، أَبُوءُ لَكَ بِنِعْمَتِكَ عَلَيَّ وَأَبُوءُ بِذَنْبِي، فَاغْفِرْ لِي، فَإِنَّهُ لَا يَغْفِرُ الذُّنُوبَ إِلَّا أَنْتَ (ثلاثًا).

اللَّهُمَّ إِنِّي أَصْبَحْتُ [أَمْسَيْتُ] مِنْكَ فِي نِعْمَةٍ وَعَافِيَةٍ وَسِتْرٍ، فَأَتِمَّ نِعْمَتَكَ عَلَيَّ وَعَافِيَتَكَ وَسِتْرَكَ فِي الدُّنْيَا وَالْآخِرَةِ (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

اللَّهُمَّ مَا أَصْبَحَ [أَمْسَى] بِي مِن نِّعْمَةٍ، أَوْ بِأَحَدٍ مِنْ خَلْقِكَ، فَمِنْكَ وَحْدَكَ لَا شَرِيكَ لَكَ، فَلَكَ الْحَمْدُ وَلَكَ الشُّكْرُ (ثَلاثًا).[تقرأ مرة واحدة للمتعجل]

يَا رَبِّ لَكَ الْحَمْدُ كَمَا يَنْبَغِي لِجَلَالِ وَجْهِكَ وَعَظِيم سُلْطَانِكَ (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

رَضِيتُ بِالله رَبًّا، وَبِالْإِسْلَامِ دِينًا، وَبسيدنا مُحَمَّدٍ صلى الله عليه وسلم نَبِيًّا ورَسُولًا (ثلاثًا).[تقرأ مرة واحدة للمتعجل]

سُبْحَانَ الله وَبِحَمْدِهِ، عَدَدَ خَلْقِهِ، وَرِضَا نَفْسِهِ، وَزِنَةَ عَرْشِهِ، وَمِدَادَ كَلِمَاتِهِ (ثلاثًا).

أَعُوذُ بِكَلِمَاتِ اللهِ التَّامَّاتِ مِنْ شَرِّ مَا خَلَقَ (ثلاثًا).

بِسْمِ الله الَّذِي لاَ يَضُرُّ مَعَ اسْمِهِ شَيْءٌ فِي الأَرْضِ وَلاَ فِي السَّمَاءِ وَهُوَ السَّمِيعُ الْعَلِيمُ (ثلاثًا).

أَعُوذُ بِالله السَّمِيعِ الْعَلِيمِ مِنَ الشَّيْطَانِ الرَّجِيمِ (ثلاثًا).

﴿هُوَ ٱللَّهُ ٱلَّذِي لَآ إِلَٰهَ إِلَّا هُوَۖ عَٰلِمُ ٱلۡغَيۡبِ وَٱلشَّهَٰدَةِۖ هُوَ ٱلرَّحۡمَٰنُ ٱلرَّحِيمُ۝٢٢هُوَ ٱللَّهُ ٱلَّذِي لَآ إِلَٰهَ إِلَّا هُوَ ٱلۡمَلِكُ ٱلۡقُدُّوسُ ٱلسَّلَٰمُ ٱلۡمُؤۡمِنُ ٱلۡمُهَيۡمِنُ ٱلۡعَزِيزُ ٱلۡجَبَّارُ ٱلۡمُتَكَبِّرُۚ سُبۡحَٰنَ ٱللَّهِ عَمَّا يُشۡرِكُونَ۝٢٣هُوَ ٱللَّهُ ٱلۡخَٰلِقُ ٱلۡبَارِئُ ٱلۡمُصَوِّرُۖ لَهُ ٱلۡأَسۡمَآءُ ٱلۡحُسۡنَىٰۚ يُسَبِّحُ لَهُۥ مَا فِي ٱلسَّمَٰوَٰتِ وَٱلۡأَرۡضِۖ وَهُوَ ٱلۡعَزِيزُ ٱلۡحَكِيمُ۝٢٤﴾ [الحشر: 22-24].

سُبْحَانَ الله وَبِحَمْدِهِ، سُبْحَانَ الله الْعَظِيمِ (ثلاثًا).

تَحَصَّنْتُ بِذِي الْعِزَّةِ وَالْجَبَرُوتِ، وَاعْتَصَمْتُ بِرَبِّ الْمَلَكُوتِ، وَتَوَكَّلْتُ عَلَى الْحَيِّ الَّذِي لَا يَمُوتُ [اصْرِفْ عَنَّا الْأَذَى إِنَّكَ عَلَى كُلِّ شَيْءٍ قَدِيرٌ (ثلاثًا)] (ثلاثًا).

بِسْمِ اللَّهِ الرَّحْمَنِ الرَّحِيمِ ﴿لِإِيلَٰفِ قُرَيۡشٍ۝١إِۦلَٰفِهِمۡ رِحۡلَةَ ٱلشِّتَآءِ وَٱلصَّيۡفِ۝٢فَلۡيَعۡبُدُواْ رَبَّ هَٰذَا ٱلۡبَيۡتِ۝٣ٱلَّذِيٓ أَطۡعَمَهُم مِّن جُوعٍ وَءَامَنَهُم مِّنۡ خَوۡفِۭ۝٤﴾ [قريش: 1-4].

